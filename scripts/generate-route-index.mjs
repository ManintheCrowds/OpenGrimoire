#!/usr/bin/env node
/**
 * Write docs/api/ROUTE_INDEX.json from App Router route.ts files under src/app/api.
 * Run after adding or moving API routes (same PR as route changes).
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const apiRoot = path.join(root, 'src', 'app', 'api');
const outFile = path.join(root, 'docs', 'api', 'ROUTE_INDEX.json');

function collectRouteFiles(dir, rel = '') {
  const out = [];
  for (const name of fs.readdirSync(dir, { withFileTypes: true })) {
    const nextRel = rel ? path.join(rel, name.name) : name.name;
    const full = path.join(dir, name.name);
    if (name.isDirectory()) {
      out.push(...collectRouteFiles(full, nextRel));
    } else if (name.name === 'route.ts') {
      const routeDir = path.dirname(nextRel);
      out.push({
        path: dirToApiPath(routeDir),
        file: path.join('src', 'app', 'api', nextRel).split(path.sep).join('/'),
      });
    }
  }
  return out;
}

function dirToApiPath(routeDir) {
  if (!routeDir || routeDir === '.') {
    return '/api';
  }
  const segments = routeDir.split(path.sep).map((seg) =>
    seg.startsWith('[') && seg.endsWith(']') ? `:${seg.slice(1, -1)}` : seg
  );
  return `/api/${segments.join('/')}`;
}

function main() {
  const routes = collectRouteFiles(apiRoot).sort((a, b) => a.path.localeCompare(b.path));
  const doc = {
    maintainer_note:
      'Generated by scripts/generate-route-index.mjs — commit with API PRs. Verified by verify-route-index.mjs.',
    generated_at: new Date().toISOString(),
    routes,
  };
  fs.mkdirSync(path.dirname(outFile), { recursive: true });
  fs.writeFileSync(outFile, `${JSON.stringify(doc, null, 2)}\n`, 'utf8');
  console.log(`Wrote ${routes.length} routes to ${path.relative(root, outFile)}`);
}

main();
